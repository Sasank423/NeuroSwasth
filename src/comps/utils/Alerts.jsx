import { toast,Bounce } from 'react-toastify';
import 'react-toastify/dist/ReactToastify.css';

export function show_error(text){
    toast.error(text, {
        position: "bottom-right",
        autoClose: 2000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "dark",
        transition: Bounce,
        });
}

